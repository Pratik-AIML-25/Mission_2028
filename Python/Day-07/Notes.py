'''
TOPIC :- Functions

✨ What is a Function?
A function is a block of reusable code.

Instead of writing the same code again and again, we write it once inside a function and call it whenever needed.

Example:

CODE:-
        def greet():
            print("Hello")

Call it:

CODE:- 
        greet()

Output:-
        Hello


✨ Syntax

CODE:-
        def function_name():
            # code

Calling

CODE:-
        function_name()


✨ Function with Parameters

CODE:-
        def greet(name):
            print("Hello", name)

        greet("Pratik")

Output:- 
        Hello Pratik


✨ Function with Return

CODE:-
        def add(a, b):
            return a + b

        result = add(5, 3)
        print(result)

Output:-
        8
'''